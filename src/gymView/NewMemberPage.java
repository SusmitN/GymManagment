    public void autoId() {
        try {
            int id=1;
            String str1=String.valueOf(id);
            memberIdLabel.setText(str1);
            String sql="select max(id) from newmembers";
            Statement smt =con.createStatement();
            ResultSet rs= smt.executeQuery(sql);
            
            if(rs.next())
                    {
                        id=rs.getInt(1);
                        id=id+1;
                        memberIdLabel.setText(Integer.toString(id));
                    }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

