    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        int id=Integer.parseInt(memberIdLabel.getText());
        String name = nameTextField.getText();
        int age = Integer.parseInt(ageTextField.getText());
        String mobileNumber = mobileNumberTextField.getText();
        String email = emailTextField.getText();
        String fathersName = fatherNameTextField.getText();
        String mothersName = motherNameTextField.getText();
        String gender = genderComboBox.getSelectedItem().toString();
        String aadharNumber = aadharNumberTextField.getText();
        String slot = slotComboBox.getSelectedItem().toString();
        int amountToPayPerMonth = Integer.parseInt(amountToPayPerMonthTextField.getText());

        try {
            Statement smt = con.createStatement();
            smt.execute("insert into newmembers (id,name, age, mobilenumber, email, fathersname, mothersname, gender, aadharnumber, slot, amounttopaypermonth, status) values (" + id + ",'" + name + "'," + age + ",'" + mobileNumber + "','" + email + "','" + fathersName + "','" + mothersName + "','" + gender + "','" + aadharNumber + "','" + slot + "'," + amountToPayPerMonth + ",1)");
            JOptionPane.showMessageDialog(this, "Record Inserted", "NewMembers", JOptionPane.INFORMATION_MESSAGE);
            smt.close();
            autoId();
            resetData();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex, "NewMemberPage", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_saveButtonActionPerformed

